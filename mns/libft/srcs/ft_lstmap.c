#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*temp;
	void	*temp_content;

	if (!lst || !f || !del)
		return (NULL);
	new_list = (NULL);
	while (lst)
	{
		temp_content = f(lst->content);
		temp = ft_lstnew(temp_content);
		if (!temp)
		{
			del(temp_content);
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, temp);
		lst = lst->next;
		temp = temp->next;
	}
	return (new_list);
}
