#include <unistd.h>

void	ft_print_comb(void)
{
	int		num;
	char	o;

	num = 11;
	while (++num < 789)
	{
		if (num / 100 < (num % 100) / 10 && (num % 100) / 10 < num % 10)
		{
			o = '0' + num / 100;
			write(1, &o, 1);
			o = '0' + (num % 100) / 10;
			write(1, &o, 1);
			o = '0' + num % 10;
			write(1, &o, 1);
			write(1, ", ", 2);
		}
	}
	write(1, "789", 3);
}
