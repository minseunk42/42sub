void	swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	sc;

	sc = size;
	i = 0;
	while (i < sc / 2)
		swap(&tab[i++], &tab[--size]);
}
